const fs = require('fs');
const path = require('path');

// Read source data
const phases = JSON.parse(fs.readFileSync(path.join(__dirname, '_data', 'phases.json'), 'utf8'));
const toolsData = JSON.parse(fs.readFileSync(path.join(__dirname, '_data', 'tools.json'), 'utf8'));

const items = [];

// Index phases
phases.forEach(phase => {
  items.push({
    title: phase.name + ' — ' + phase.short,
    desc: phase.description,
    text: [phase.name, phase.short, phase.description, ...(phase.questions || [])].join(' '),
    type: 'phase',
    href: '/' + phase.id + '/',
    icon: phase.icon
  });
  (phase.skills || []).forEach(skill => {
    items.push({
      title: skill.name,
      cmd: skill.cmd,
      desc: skill.desc,
      text: [skill.name, skill.cmd, skill.desc, skill.when || ''].join(' '),
      type: 'skill',
      href: '/' + phase.id + '/',
      phase: phase.name
    });
  });
});

// Index tools
(toolsData.sections || []).forEach(section => {
  (section.items || []).forEach(tool => {
    items.push({
      title: tool.title,
      cmd: tool.cmd,
      desc: tool.desc,
      text: [tool.icon, tool.title, tool.cmd, tool.desc].join(' '),
      type: 'tool',
      href: '/tools/'
    });
  });
});

// Write as a JS file that sets window.SEARCH_INDEX
const jsContent = '// Auto-generated by build-search-index.js\nwindow.SEARCH_INDEX = ' + JSON.stringify(items, null, 2) + ';\n';
const outPath = path.join(__dirname, 'js', 'search-data.js');
fs.writeFileSync(outPath, jsContent, 'utf8');
console.log('[build] Generated js/search-data.js (' + items.length + ' items)');
